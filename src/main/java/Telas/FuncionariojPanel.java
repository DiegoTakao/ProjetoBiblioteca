    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        Funcionario funcionario = new Funcionario();
        funcionario.setID(jTextID.getText());
        funcionario.setNome(jTextNome.getText());
        funcionario.setCPF(jTextCPF.getText());
        funcionario.setTelefone(jTextTelefone.getText());
        
        try {
            data = SDF.parse(jTextAdmissao.getText());
        } catch (ParseException ex) {
            Logger.getLogger(FuncionariojPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        funcionario.setAdmissao(data);
        
        if(F.procuraID(funcionario.getID()) == null){
            F.inserir(funcionario);
        }else{
            F.editar(funcionario);
        }
        
        jButtonLimparActionPerformed(evt);
        carregarTabela();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int linha = Tabela.getSelectedRow();
        
        if(linha >= 0){
            String id = (String) Tabela.getValueAt(linha, 0);
            Funcionario funcionario = F.procuraID(id);
            
            jTextID.setText(funcionario.getID());
            jTextNome.setText(funcionario.getNome());
            jTextCPF.setText(funcionario.getCPF());
            jTextTelefone.setText(funcionario.getTelefone());
            jTextAdmissao.setText(SDF.format(funcionario.getAdmissao()));
            
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        int[] linhas = Tabela.getSelectedRows();
        for (int linha : linhas) {
            String id = (String) Tabela.getValueAt(linha, 0);
            F.remover(id);
        }
        carregarTabela();
    }//GEN-LAST:event_jButtonExcluirActionPerformed

