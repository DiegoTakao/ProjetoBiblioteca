    private void carregarTabela(){
        List<Funcionario> lista = F.listar();
        
        //Limpar a tabela
        DefaultTableModel DFM = (DefaultTableModel) Tabela.getModel();
        int RC = DFM.getRowCount();
        for (int i = 0; i < RC; i++) {
            DFM.removeRow(0);
        }
        
        for (Funcionario funcionario : lista) {
            Object[] linha = new Object[5];
            linha[0] = funcionario.getID();
            linha[1] = funcionario.getNome();
            linha[2] = funcionario.getCPF();
            linha[3] = funcionario.getTelefone();
            linha[4] = funcionario.getAdmissao();
            
            DFM.addRow(linha);
        }
    }

