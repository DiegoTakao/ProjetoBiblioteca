    private void carregarTabela(){
        List<Livro> lista = L.listar();
        
        //Limpar a tabela
        DefaultTableModel DFM = (DefaultTableModel) Tabela.getModel();
        int RC = DFM.getRowCount();
        for (int i = 0; i < RC; i++) {
            DFM.removeRow(0);
        }
        
        for (Livro livro : lista) {
            Object[] linha = new Object[4];
            linha[0] = livro.getID();
            linha[1] = livro.getISBN();
            linha[2] = livro.getTitulo();
            linha[3] = livro.getAno();
            
            DFM.addRow(linha);
        }
    }

