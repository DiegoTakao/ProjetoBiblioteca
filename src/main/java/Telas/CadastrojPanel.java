    private void carregarTabela(){
        List<Cadastro> lista = C.listar();
        
        //Limpar a tabela
        DefaultTableModel DFM = (DefaultTableModel) Tabela.getModel();
        int RC = DFM.getRowCount();
        for (int i = 0; i < RC; i++) {
            DFM.removeRow(0);
        }
        
        for (Cadastro cadastro : lista) {
            Object[] linha = new Object[4];
            linha[0] = cadastro.getID();
            linha[1] = cadastro.getNome();
            linha[2] = cadastro.getCPF();
            linha[3] = cadastro.getTelefone();
            
            DFM.addRow(linha);
        }
    }

