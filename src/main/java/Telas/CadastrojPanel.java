    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        Cadastro cadastro = new Cadastro();
        cadastro.setID(jTextID.getText());
        cadastro.setNome(jTextNome.getText());
        cadastro.setCPF(jTextCPF.getText());
        cadastro.setTelefone(jTextTelefone.getText());
        
        if(C.procuraID(cadastro.getID()) == null){
            C.inserir(cadastro);
        }else{
            C.editar(cadastro);
        }
        
        jButtonLimparActionPerformed(evt);
        carregarTabela();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int linha = Tabela.getSelectedRow();
        
        if(linha >= 0){
            String id = (String) Tabela.getValueAt(linha, 0);
            Cadastro cadastro = C.procuraID(id);
            
            jTextID.setText(cadastro.getID());
            jTextNome.setText(cadastro.getNome());
            jTextCPF.setText(cadastro.getCPF());
            jTextTelefone.setText(cadastro.getTelefone());
            
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        int[] linhas = Tabela.getSelectedRows();
        for (int linha : linhas) {
            String id = (String) Tabela.getValueAt(linha, 0);
            C.remover(id);
        }
        carregarTabela();
    }//GEN-LAST:event_jButtonExcluirActionPerformed


